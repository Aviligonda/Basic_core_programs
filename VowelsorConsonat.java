public class VowelsorConsonat {
    public static void main(String[] args) {
        char alpha='c';
        if(alpha=='a'||alpha=='e'||alpha=='i'||alpha=='o'||alpha=='u' ) {
            System.out.println(alpha + "--> is a vowel");
        }else {
            System.out.println(alpha+"--> is a consonat");
        }
    }
}
